class Typecasting{
	
	
	public static void main(String args[]){
		
		int x=14677899;//assigning the value of x
		double y=x;//converting int(x)to double (y)
		long z=(long)y;//double(y) to long and storing in variable z of type long
		
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		
	}
}
